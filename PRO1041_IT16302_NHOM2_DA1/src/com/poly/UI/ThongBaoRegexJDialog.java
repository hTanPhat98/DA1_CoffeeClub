    public void alert(int i, String message) {
        this.setLocationRelativeTo(null);
        lblTagName.setText("Thông Báo");
        lblIcon.setText("");
        txtMess.setText(message);
        pnlMessInfo.setVisible(true);
        switch (i) {
            case 1:
                lblIcon.setIcon(XImage.readMess("confirm_mess_x32.png"));
                break;
            case 2:
                lblIcon.setIcon(XImage.readMess("warning_mess_x32.png"));
                break;
            
            default:
                break;
        }
        
        this.setVisible(true);
        
    }
    
    

